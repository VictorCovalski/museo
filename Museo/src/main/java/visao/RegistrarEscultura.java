    public ArrayList<String> getjComboBoxAutores() {
        ArrayList<String> autores = new ArrayList<>();
        for(int i=1;i<jComboBoxAutores.getItemCount();i++){
            autores.add(jComboBoxAutores.getItemAt(i));
        }
        return autores;
    }

